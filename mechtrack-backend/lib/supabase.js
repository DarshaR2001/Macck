const { createClient } = require('@supabase/supabase-js');
require('dotenv').config();

const supabaseUrl = process.env.SUPABASE_URL;
const supabaseServiceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!supabaseUrl || !supabaseServiceKey || supabaseServiceKey.includes('YOUR_SUPABASE_SERVICE_ROLE_KEY')) {
  console.warn("⚠️ Warning: SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY must be configured in your backend .env file!");
}

const supabase = createClient(supabaseUrl, supabaseServiceKey, {
  auth: {
    autoRefreshToken: false,
    persistSession: false
  }
});

module.exports = { supabase };
